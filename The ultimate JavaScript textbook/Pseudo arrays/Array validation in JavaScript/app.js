// Дано двовимірний масив:    Переберіть цей масив циклом і для кожного елемента перевірте, масив він чи ні.

let test = [
    [1, 2, 3],
    { a: 1, b: 2, c: 3 },
    [3, 4, 5],
    { x: 1, y: 2, z: 3 },
];

for (let i = 0; i < test.length; i++) {
    if (Array.isArray(test[i])) {
        console.log(`Елемент з індексом ${i} є масивом:`, test[i]);
    } else {
        console.log(`Елемент з індексом ${i} НЕ є масивом:`, test[i]);
    }
}
