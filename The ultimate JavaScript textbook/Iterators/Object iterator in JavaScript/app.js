// Зробіть ітератор, який перебиратиме об'єкт і кожним викликом повертатиме масив, у нульовому елементі якого лежатиме ключ, а в першому елементі - значення елемента об'єкта. Приклад:
function* func(obj) {
    for (let key in obj) {
        yield [key, obj[key]];
    }
}
let iter = func({ a: 1, b: 2, c: 3 });

for (let elem of iter) {
    console.log(elem); // ['a', 1], ['b', 2], ['c', 3]
}
