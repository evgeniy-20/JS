// Додайте в об'єкт функцію, яка виводитиме поточний час. Викличте цю функцію.

// Створюємо об'єкт
let obj = {
    name: 'Євген',
    age: 27
};

// Створюємо символ для прихованої функції
let showTime = Symbol('поточний час');

// Додаємо функцію до об'єкта
obj[showTime] = function () {
    let now = new Date();
    console.log('Поточний час:', now.toLocaleTimeString());
};

// Викликаємо функцію
obj[showTime](); // Наприклад: Поточний час: 14:32:10
