// Зробіть об'єкт із ключем із символу. Переберіть його циклом. Переконайтеся, що символ не бере участі в переборі.

let sym = Symbol('прихований ключ');
let obj = {
    a: 10,
    b: 20,
    c: 30
}

for (let key in obj) {
    console.log(key, obj[key]);
}
// Перевірка, що символ не перебрався:
console.log('Символи у об\'єкті:', Object.getOwnPropertySymbols(obj));
// Виведе: [ Symbol(прихований ключ) ]

console.log('Значення за символом:', obj[sym]);
// Виведе: Це секрет
