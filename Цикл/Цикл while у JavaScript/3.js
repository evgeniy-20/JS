// Дано число num з деяким початковим значенням. Помножуйте його на 3стільки разів, поки результат множення не стане більшим 1000. Яке число вийде? Порахуйте кількість і терацій, необхідних цього.

let num = 10;
let i = 0;

while (num <= 1000) {
    num *= 3;
    i++
}

console.log("Число:", num);
console.log("Кількість ітерацій:", i);
