// Даний наступний масив: let arr = ['a', 'b', 'c']; Дана також змінна: let key = 2;
// Виведіть на екран елемент, який зберігається у змінній key.
let arr = ['a', 'b', 'c'];
let key = 2;
console.log(arr[key]);
// Даний наступний масив: let arr = [1, 2, 3, 4, 5]; Дано також змінні:
// let key1 = 1;
