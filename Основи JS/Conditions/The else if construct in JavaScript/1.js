// У змінній day лежить якесь число з інтервалу від 1до 31. Визначте, у яку декаду місяця потрапляє це число (у першу, другу чи третю).

let day = 10;

if(day >= 1 &&  day <= 10) {
    console.log('першу');
} else if(day >= 11 && day <= 20) {
    console.log('другу');
} else if(day >= 21 && day <= 31) {
    console.log('третю');
} else {
    console.log('Такого числа в місяці не існує!!!');
}