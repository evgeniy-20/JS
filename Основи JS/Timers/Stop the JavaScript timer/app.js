//Нехай дана змінна, де спочатку зберігається число 10. Запустіть таймер, який кожну секунду буде зменшувати значення цієї змінної на 1 і виводити це значення в консоль. Як тільки значення змінної досягне нуля – зупиніть таймер.

let i = 10;

let timerId = setInterval(function() {
	console.log(--i);
	
	if (i === 0) {
		clearInterval(timerId);
	}
}, 1000);