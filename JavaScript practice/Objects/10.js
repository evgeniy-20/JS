// Напишіть функцію, яка приймає об'єкт і повертає масив усіх його ключів.
// Створюємо функцію, яка приймає щб'єкт
function getObj(obj) {
    let arr = []; // створюємо масив для додавання в нього ключів об'єкта
    // Створюємо цикл для проходження по об'єкту
    for (let key in obj) {
        arr.push(key); //  до даємо ключі до масиву
    }
    // повертаємо масив ключів об'єкта
    return arr;
}


// виводимо результат в консоль

console.log(getObj({ a: 1, b: 2, c: 3 }))
