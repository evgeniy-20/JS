// Створіть новий об'єкт, де ключі беруться з одного масиву, а значення — з іншого.

let arr1 = [1, 2, 3, 4, 5, 6, 7];
let arr2 = ['Пн', 'Вт', 'Ср', 'Чт', 'Пт', 'Сб', 'Нд'];

let dayValue = {};

// Створюємо цикл за допомогою якого будемо проходити по об'єкту

for (let i = 0; i < arr1.length; i++) {
    dayValue[arr1[i]] = arr2[i];
};

console.log(dayValue);
