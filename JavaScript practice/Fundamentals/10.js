// Об'єкт і функції Створи об'єкт user із властивостями: ім'я, вік, місто. Додай до нього метод sayHello, який виводить у консоль текст типу "Привіт, мене звати Євген. Мені 25 років і я з Києва.".

let user = {
    name: 'Yevhen',
    age: 27,
    city: 'Kyiv',
    sayHello: function () {
        console.log(`Привіт, мене звати ${this.name}. Мені ${this.age} років і я з ${this.city}.`);
    }
};

// Виклик методу
user.sayHello();
