let arr = [];

for (let i = 0, k = 1; i < 2; i++) {
    arr[i] = [];
    for (let j = 0; j < 2; j++) {
        arr[i][j] = []
        for (let s = 0; s < 2; s++) {
            arr[i][j].push(k++);
        }
    }
}

