// Отримайте об'єкт із датою, що містить поточний момент часу. Отримайте об'єкт із датою, що містить полудень поточного дня. Порівняйте два ці об'єкти і визначте, чи був уже полудень, чи ні.
const now = new Date(); // Поточний момент часу

const noon = new Date(); // Копіюємо поточний день
noon.setHours(12, 0, 0, 0); // Встановлюємо час на 12:00:00.000

if (now >= noon) {
    console.log("Полудень уже був.");
} else {
    console.log("Полудень ще не настав.");
}
