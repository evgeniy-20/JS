// Отримайте об'єкт із датою, що містить поточний момент часу. Отримайте об'єкт із датою, що містить 15 число поточного місяця. Порівняйте два ці об'єкти і визначте, пройшла вже половина місяця чи ні.

const now = new Date(); // Поточний момент часу

const midMonth = new Date(now.getFullYear(), now.getMonth(), 15); // 15 число поточного місяця

if (now >= midMonth) {
    console.log("Половина місяця вже пройшла.");
} else {
    console.log("Половина місяця ще не пройшла.");
}
