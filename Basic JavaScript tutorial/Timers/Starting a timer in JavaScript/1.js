// Виведіть у консоль число 0. Через секунду виведіть число 1, через дві секунди виведіть число 2, через 3 секунди виведіть число 3. І так далі до нескінченнос
let i = 0;

function logNumber() {
    console.log(i);
    i++;
    setTimeout(logNumber, i * 1000); // Кожного разу збільшуємо інтервал на 1 секунду
}

logNumber();
