// Нехай дано змінну, у якій спочатку зберігається число 10. Запустіть таймер, який щосекунди зменшуватиме значення цієї змінної на 1 і виводитиме це значення в консоль. Як тільки значення змінної досягне нуля - зупиніть таймер.

let num = 10;
let timerID = setInterval(() => {
    console.log(num);
    num--;
    if (num <= 0) {
        clearInterval(timerID);
    }
}, 1000);
