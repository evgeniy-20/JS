// Нехай у вас є функція, що повертає квадратний корінь з числа, і функція, що округляє дріб до трьох знаків у дрібній частині:

function sqrt(num) {
    return Math.sqrt(num);
}

function round(num) {
    return num.toFixed(3);
}

// За допомогою цих функцій знайдіть квадратний корінь із числа 2 та округліть його до трьох знаків у дрібній частині.

let res = round(sqrt(2));
console.log(res);
