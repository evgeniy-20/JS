// Напишіть функцію, яка параметром прийматиме число і ділитиме його на 2 стільки разів, поки результат не стане меншим за 10. Нехай функція повертає кількість ітерацій, яка знадобилася для досягнення результату.

function getNum(num) {
    let count = 0;
    while (num >= 10) {
        num /= 2;
        count++;
    }
    return count;
}

console.log(getNum(15));
