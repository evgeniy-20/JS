// Перепишіть наступний код через switch-case:

let lang = 'ua';

switch (lang) {
    case 'ua':
        console.log('ukranian');
        break;
    case 'en':
        console.log('english');
        break;
    case 'de':
        console.log('germany');
        break;
    default:
        console.log('мова не знайдена!!!');
        break;
}
