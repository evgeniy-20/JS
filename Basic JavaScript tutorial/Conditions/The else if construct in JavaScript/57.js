let day = 45;

if (day >= 0 && day <= 10) {
    console.log('Перша декада!');
} else if (day >= 11 && day <= 20) {
    console.log('Друга декада!');
} else if (day >= 21 && day <= 31) {
    console.log('Третя декада!');
} else {
    console.log('Такої декади немає!!!');
}
