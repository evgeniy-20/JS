// Модифікуйте попереднє завдання так, щоб, якщо в змінній day буде не число від 1 до 31, видавалося повідомлення про помилку.

let day = 65;
if (day >= 1 && day <= 10) {
    console.log('Перша декада місяця');
} else if (day >= 11 && day <= 20) {
    console.log('Друга декада місяця');
} else if (day >= 21 && day <= 31) {
    console.log('Третя декада місяця')
} else {
    console.log('Помилка');
}
