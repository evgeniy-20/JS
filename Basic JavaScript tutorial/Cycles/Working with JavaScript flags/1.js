// Даний масив: Перевірте, що в цьому масиві є елемент 'c'. Якщо є – виведіть '+++', а якщо ні – виведіть '---'.

let arr = ['a', 'b', 'c', 'd', 'e'];
let found = false; // Прапор, який спочатку встановлений у false

for (let i = 0; i < arr.length; i++) {
    if (arr[i] === 'c') {
        found = true; // Якщо знайдено 'c', змінюємо прапор на true
        break; // Виходимо з циклу, бо далі шукати немає сенсу
    }
}

if (found) {
    console.log('+++');
} else {
    console.log('---');
}
