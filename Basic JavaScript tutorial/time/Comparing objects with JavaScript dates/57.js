// Отримайте об'єкт із датою, що містить поточний час. Отримайте об'єкт із датою, яка містить 15поточний місяць. Порівняйте два ці об'єкти і визначте, чи минула вже половина місяця чи ні.

// Отримати поточний час
const currentDate = new Date();

// Отримати об'єкт дати, що містить 15-те число поточного місяця
const midMonthDate = new Date(currentDate.getFullYear(), currentDate.getMonth(), 15);

// Порівняти дати
if (currentDate > midMonthDate) {
    console.log("Половина місяця вже минула.");
} else {
    console.log("Половина місяця ще не минула.");
}
