// Даний масив: За допомогою рекурсії виведіть елементи масиву в консоль.

let arr = [1, 2, 3, 4, 5];
function printArray(arr, index) {
    // Базовий випадок: якщо індекс перевищив розмір масиву, закінчити рекурсію
    if (index >= arr.length) {
        return;
    }

    // Виводимо поточний елемент масиву
    console.log(arr[index]);

    // Викликаємо функцію рекурсивно для наступного елементу
    printArray(arr, index + 1);
}

// Викликаємо функцію з початковим індексом 0
printArray(arr, 0);
