// У змінній day лежить якесь число з інтервалу від 1 до 31. Визначте, у яку декаду місяця потрапляє це число (у першу, другу або третю).

let day = 10;
if (day >= 1 && day <= 10) {
    console.log('перша');
} else if (day >= 11 && day <= 20) {
    console.log('друга');
} else if (day >= 21 && day <= 31) {
    console.log('третя');
}
