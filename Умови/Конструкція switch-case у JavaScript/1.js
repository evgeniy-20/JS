// Перепишіть наступний код через switch-case:

let lang = 'ua';

switch (lang) {
    case 'ua':
        console.log('укр');
        break;
    case 'en':
        console.log('анг');
        break;
    case 'de':
        console.log('нім');
        break;
    default:
        console.log('мова не підтримується');
        break;
}
